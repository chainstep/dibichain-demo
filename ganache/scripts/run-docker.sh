#!/bin/bash

# Starts the ganache test chain

###################################################################################################
# CONFIGURATION
###################################################################################################

PROJECT_NAME="dibichain-ganache"

RUN_DETACHED=true


###################################################################################################
# PARAMETER PARSING
###################################################################################################

while getopts "h?b?" opt; do
    case "$opt" in
        h)
            echo "Parameter: [<value> / (flag)]"
            echo "-b (run in blocking mode)"
            exit 0
            ;;
        b)
            RUN_DETACHED=false
            ;;
    esac
done


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(pwd)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

SUDO=""
if [ $(uname) == Linux ]; then
    SUDO="sudo"
fi

DETACHED=""
if [ ${RUN_DETACHED} == true ]; then 
    DETACHED="-d";
fi

cd ${HERE}/../docker
${SUDO} docker-compose -p ${PROJECT_NAME} up ${DETACHED}