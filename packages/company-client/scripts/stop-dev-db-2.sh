#!/bin/bash

# Stops and removes the development database container

###################################################################################################
# CONFIGURATION
###################################################################################################

CONTAINER_NAME="company-backend-dev-db-2"


###################################################################################################
# DEFINES
###################################################################################################

HERE="$(pwd)/$(dirname $0)"


###################################################################################################
# MAIN
###################################################################################################

SUDO=""
if [ $(uname) == Linux ]; then
    SUDO="sudo"
fi

${SUDO} docker container stop ${CONTAINER_NAME}
${SUDO} docker container rm ${CONTAINER_NAME}